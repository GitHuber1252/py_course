from .EscalatorBase import EscalatorBase

class N_series(EscalatorBase):
    """
    Эскалаторы серии Н (1935-1949).
    Модели: Н-10, Н-20, Н-30, Н-40.
    Особенности: деревянные ступени и перила, высокая надёжность, большой срок службы.
    """
    characteristics_names = [
        "Максимальная высота подъема (м)",
        "Скорость движения (м/с)",
        "Пропускная способность (чел/час)",
        "Энергопотребление (кВт·ч)",
        "Уровень шума (дБ)",
        "Срок службы (лет)"
    ]
    
    # Веса для серии Н: акцент на долговечность и простоту
    weights = {
        "Максимальная высота подъема (м)": 0.10,
        "Скорость движения (м/с)": 0.15,
        "Пропускная способность (чел/час)": 0.20,
        "Энергопотребление (кВт·ч)": 0.15,
        "Уровень шума (дБ)": 0.15,
        "Срок службы (лет)": 0.25
    }
    
    reference_ranges = {
        "Максимальная высота подъема (м)": (9, 40),
        "Скорость движения (м/с)": (0.5, 0.75),
        "Пропускная способность (чел/час)": (4000, 8000),
        "Энергопотребление (кВт·ч)": (10, 25),
        "Уровень шума (дБ)": (70, 85),
        "Срок службы (лет)": (40, 80)  # исторически служили очень долго
    }
    
    @classmethod
    def from_input(cls):
        name = input("Введите модель серии Н (Н-10, Н-20, Н-30, Н-40): ")
        chars = {}
        for char in cls.characteristics_names:
            prompt = f"{char} (диапазон {cls.reference_ranges[char]}): "
            val_str = input(prompt).strip()
            if val_str == "":
                val = (cls.reference_ranges[char][0] + cls.reference_ranges[char][1]) / 2
                print(f"  Принято по умолчанию: {val}")
            else:
                val = float(val_str)
            chars[char] = val
        return cls(name, chars)