from .EscalatorBase import EscalatorBase

class E_series(EscalatorBase):
    """
    Эскалаторы серии Е (2000-2012).
    Модели: Е-25Т, Е-55Т, Е-75Т, Е-900Т.
    Современный дизайн, улучшенная энергоэффективность.
    """
    characteristics_names = EscalatorBase.characteristics_names
    
    weights = {
        "Максимальная высота подъема (м)": 0.15,
        "Скорость движения (м/с)": 0.20,
        "Пропускная способность (чел/час)": 0.25,
        "Энергопотребление (кВт·ч)": 0.20,
        "Уровень шума (дБ)": 0.10,
        "Срок службы (лет)": 0.10
    }
    
    reference_ranges = {
        "Максимальная высота подъема (м)": (5, 75),
        "Скорость движения (м/с)": (0.5, 0.75),
        "Пропускная способность (чел/час)": (5000, 12000),
        "Энергопотребление (кВт·ч)": (5, 15),
        "Уровень шума (дБ)": (55, 70),
        "Срок службы (лет)": (30, 50)
    }
    
    @classmethod
    def from_input(cls):
        name = input("Введите модель серии Е (Е-25Т, Е-55Т, Е-75Т, Е-900Т): ")
        chars = {}
        for char in cls.characteristics_names:
            val = float(input(f"Введите {char}: "))
            chars[char] = val
        return cls(name, chars)