from .EscalatorBase import EscalatorBase

class Modern_series(EscalatorBase):
    """
    Современные эскалаторы (с 2013).
    Модели: ТК-65, ЭС, FT-935 Victoria, FT-955 Victoria Tube, Schindler, ETK.
    Высокая энергоэффективность, светодиодная подсветка, возможность сужения балюстрады.
    """
    characteristics_names = EscalatorBase.characteristics_names + [
        "Наличие энергосберегающего режима (0/1)",
        "Наличие светодиодной подсветки (0/1)"
    ]
    
    weights = {
        "Максимальная высота подъема (м)": 0.12,
        "Скорость движения (м/с)": 0.18,
        "Пропускная способность (чел/час)": 0.20,
        "Энергопотребление (кВт·ч)": 0.15,
        "Уровень шума (дБ)": 0.10,
        "Срок службы (лет)": 0.10,
        "Наличие энергосберегающего режима (0/1)": 0.10,
        "Наличие светодиодной подсветки (0/1)": 0.05
    }
    
    reference_ranges = {
        "Максимальная высота подъема (м)": (5, 100),
        "Скорость движения (м/с)": (0.5, 1.0),
        "Пропускная способность (чел/час)": (6000, 15000),
        "Энергопотребление (кВт·ч)": (4, 12),
        "Уровень шума (дБ)": (50, 65),
        "Срок службы (лет)": (35, 60),
        "Наличие энергосберегающего режима (0/1)": (0, 1),
        "Наличие светодиодной подсветки (0/1)": (0, 1)
    }
    
    inverse_indicators = EscalatorBase.inverse_indicators.copy()
    # бинарные показатели - больше лучше, оставляем как есть
    
    @classmethod
    def from_input(cls):
        name = input("Введите модель современного эскалатора: ")
        chars = {}
        for char in cls.characteristics_names:
            if char.startswith("Наличие"):
                val = int(input(f"{char} (0 – нет, 1 – есть): "))
            else:
                val = float(input(f"Введите {char}: "))
            chars[char] = val
        return cls(name, chars)